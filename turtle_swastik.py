t.right(90)
t.forward(100)
t.penup()
t.goto(0, 0)
t.pendown()
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
t.penup()
t.goto(0, 0)
t.pendown()
t.forward(100)
t.right(90)
t.forward(100)
t.penup()
